n1 = int(input("Digite um numero inteiro: "))
n2 = int(input("Digite outro valor inteiro: "))

if n1 > n2:
    print("O primeiro valor é maior Valor: {}}".format(n1))

elif n1 < n2:
    print("O segundo valor é maior Valor: {}".format(n2))

else:
    print("Os dois são iguais, não existe valor maior {} = {}".format(n1, n2))